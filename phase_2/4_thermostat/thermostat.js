class Thermostat {
    constructor() {
      this.temperature = 20;
    }
  
    getTemperature() {
      return this.temperature;
    }

    up() {
      return this.temperature += 1  
    }

    down() {
        if (this.temperature = 22) {
          this.temperature -= 1;
        }
      }
      
      
      
      
      
      

  }
  
  module.exports = Thermostat;
  
  